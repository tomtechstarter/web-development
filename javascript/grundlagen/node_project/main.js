function celsiusToFahrenheit(celsius){
    let fahrenheit = celsius * 9 / 5 + 32;
    return fahrenheit;
}

console.log(celsiusToFahrenheit(21))    // 69.8 째F
console.log(celsiusToFahrenheit(20))    // 68   째F
console.log(celsiusToFahrenheit(-10))   // 14   째F
console.log(celsiusToFahrenheit(0))     // 32   째F